package day2;

public class Student {
	private int sid;
	private char stype;
	public void setStudentId(int sid) {
		this.sid = sid;
	}
	public void setStudentType(char stype) {
		this.stype = stype;
	}
	public int getStudentId() {
		return this.sid;
	}
	public char getStudentType() {
		return this.stype;
	}

}
